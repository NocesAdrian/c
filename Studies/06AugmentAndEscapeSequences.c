// standard input/output library 
// stdio.h
#include <stdio.h>

int main() {
	// this is comment
	
	// this is tab \t
	printf("this\tis\ttab\n\n");
	
	// this is newline \n
	printf("this\nis\nnewline\n\n");
	
	// this is backslach \
	// \"backslash\" -> "backslash" to use "" on words
	printf("this is \"backslash\"\n");
	
	//much better approach
	// -> 'backslash'
	printf("this is 'backslash'\n");
	
	/*
	  this
	  is
	  multiline
	  comment
	
	*/
	
	return 0;
}
