#include <stdio.h>
#include <string.h>

int main()
{
    // String Manipulation Functions

    // strlen() – Returns the length of a string (excluding the null terminator)
    strlen();  // Get the length of a string

    // strcpy() – Copies one string to another
    strcpy();  // Copy one string into another

    // strncpy() – Copies a specified number of characters from one string to another
    strncpy(); // Copy a specified number of characters

    // strcat() – Concatenates (appends) two strings
    strcat();  // Append one string to another

    // strncat() – Concatenates a limited number of characters from one string to another
    strncat(); // Append a specified number of characters

    // strcmp() – Compares two strings (returns 0 if equal)
    strcmp();  // Compare two strings

    // strncmp() – Compares a limited number of characters from two strings
    strncmp(); // Compare a specified number of characters

    // strchr() – Finds the first occurrence of a character in a string
    strchr();  // Search for the first occurrence of a character

    // strrchr() – Finds the last occurrence of a character in a string
    strrchr(); // Search for the last occurrence of a character

    // strstr() – Finds the first occurrence of a substring in a string
    strstr();  // Search for a substring in a string

    // strtok() – Splits a string into tokens using delimiters
    strtok();  // Tokenize a string using delimiters

    // Memory Manipulation Functions

    // memcpy() – Copies a block of memory
    memcpy();  // Copy a block of memory from source to destination

    // memmove() – Moves a block of memory safely (handles overlapping)
    memmove(); // Move memory blocks safely

    // memset() – Fills a block of memory with a specified value
    memset();  // Fill memory with a specific value

    // memcmp() – Compares two memory blocks
    memcmp();  // Compare two memory blocks

    return 0;
}
